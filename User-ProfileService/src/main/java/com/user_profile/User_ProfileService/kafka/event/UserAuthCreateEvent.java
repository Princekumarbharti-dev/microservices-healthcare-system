package com.user_profile.User_ProfileService.kafka.event;

public class UserAuthCreateEvent {
    private Integer userId;
    private String email;
    private String password;
    private String role;

    public UserAuthCreateEvent() {
    }

    public UserAuthCreateEvent(Integer userId, String email, String password, String role) {
        this.userId = userId;
        this.email = email;
        this.password = password;
        this.role = role;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
